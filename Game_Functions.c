void Game_Level3EnemyMove(void){
	int8_t x_move;
	
	if(enemyMoveCnt > (5/levelCycle)){
		enemyMoveCnt = 0;

		if(Enemy3.life > 0){
			x_move = randomSeed % 2;

			if(enemyMoveCntY > 200){
			    enemyMoveCntY = 0;
			    Enemy3.y++;
			}
			else{
			    enemyMoveCntY++;
			}

			if(Enemy3.x == 68){
			    goingRight = false;
			}
			else if(Enemy3.x == 4){
			    goingRight = true;
			}

			if(((Enemy3.x > 1) && (Enemy3.x < 69)) && (goingRight == true)){
				Enemy3.x++;
			}
			else if(((Enemy3.x > 1) && (Enemy3.x < 69)) && (goingRight == false)){
                Enemy3.x--;
            }
				
			if(enemyAnimation > 2){
			    enemyAnimation = 0;
                if(Enemy3.image == SmallEnemy10PointA){
                    Enemy3.image = SmallEnemy10PointB;
                }
                else{
                    Enemy3.image = SmallEnemy10PointA;
                }
			}
			else{
			    enemyAnimation++;
			}
		}
	}
	else{
		enemyMoveCnt++;
		enemyMoveCntY++;
	}
	
	if(enemyLaserFreq > (200/levelCycle)){
		enemyLaserFreq = 0;
		Random_Init((x_move+randomSeed));
		if((Enemy3.life > 0)){
			enemyLaser[0].x = Enemy3.x + 7;
			enemyLaser[0].y = Enemy3.y + 5;
			enemyLaser[0].life = 1;
		}
		else{
			enemyLaser[0].x = 100;
			enemyLaser[0].y = 0;
			enemyLaser[0].life = 0;
		}
	}
	else{
		enemyLaserFreq++;
	}
	
}


