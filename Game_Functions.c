void Game_PlayerMove(uint16_t slideVal){
	int i;
	
	if(randomSeed >= 254){
		randomSeed = 0;
	}
	else{
		randomSeed++;
	}
	
	Player.x = (slideVal/61);
	Player.y = 47;
	Player.image = PlayerShip0;
	Player.life = 1;

	if(laserCnt > 1){
	    laserCnt = 0;
	    for(i = 0; i < 3; i++){
	        if(Laser3[i].life > 0){
	            laserCnt = 0;
	            Laser3[i].y--;
	        }
	     }
	}
	else{
	    laserCnt++;
	}
	
	if(Missile.life > 0){
		if(missileCnt > 1){
			missileCnt = 0;
			Missile.y--;
		}
		else{
			missileCnt++;
		}
	}
	
	if(enemyLaserCnt > (4/levelCycle)){
		enemyLaserCnt = 0;
		if(levelCycle <= 3){
            for(i = 0; i < 5; i++){
                if(enemyLaser[i].life > 0){
                    enemyLaser[i].y++;
                }
            }
		}
		else if(levelCycle <= 7){
		    for(i = 0; i < 5; i++){
		        if(enemyLaser[i].life > 0){
		            enemyLaser[i].y += 2;
		        }
		    }
		}
		else{
		    for(i = 0; i < 5; i++){
		        if(enemyLaser[i].life > 0){
		            enemyLaser[i].y += 3;
		        }
		    }
		}
	}
	else{
			enemyLaserCnt++;
	}
	
	for(i = 0; i < 3; i++){
	    if(Laser3[i].y <= 5){
	        Laser3[i].life = 0;
	    }
	}

	if(Missile.y <= 5){
		Missile.life = 0;
	}
}

void Game_Level1EnemyMove(void){
	int i;
	int8_t x_move;
	
	if(enemyMoveCnt > (200/levelCycle)){
		enemyMoveCnt = 0;
		for(i = 0;i < 4; i++){
			if(Enemy1[i].life > 0){
				if((Enemy1[i].x >= (21*i)) && (Enemy1[i].x <= ((21*i)+4))){
					x_move = randomSeed % 2;
					Enemy1[i].x += x_move;
					Enemy1[i].y += 1;
				}
				else if(Enemy1[i].x < (21*i)){
					Enemy1[i].x += 1;
					Enemy1[i].y += 1;
				}
				else if(Enemy1[i].x > ((21*i)+4)){
					Enemy1[i].x -= 2;
					Enemy1[i].y += 1;
				}
				else{
					Enemy1[i].life = 0;
				}
				
				if(Enemy1[i].image == SmallEnemy30PointA){
					Enemy1[i].image = SmallEnemy30PointB;
				}
				else{
					Enemy1[i].image = SmallEnemy30PointA;
				}
			}
		}
	}
	else{
		enemyMoveCnt++;
	}
	
	if(enemyLaserFreq > (400/levelCycle)){
		enemyLaserFreq = 0;
		enemyLaserRand = (x_move+randomSeed) % 4;
		for(i = 0;i < 4; i++){
			if((Enemy1[i].life > 0) && (i == enemyLaserRand)){
				enemyLaser[i].x = Enemy1[i].x + 7;
				enemyLaser[i].y = Enemy1[i].y + 5;
				enemyLaser[i].life = 1;
			}
			else{
				enemyLaser[i].x = 100;
				enemyLaser[i].y = 0;
				enemyLaser[i].life = 0;
			}
		}
	}
	else{
		enemyLaserFreq++;
	}
	
}

